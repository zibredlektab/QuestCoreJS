// This creates a function $.classes() which returns an array of classes for an object.
// Why this is not a built-in part of jQuery I have no idea.
// Thank you to user Will (http://stackoverflow.com/users/1185698/will) on stackoverflow
// for the plugin...

;!(function ($) {
    $.fn.classes = function (callback) {
        var classes = [];
        $.each(this, function (i, v) {
            var splitClassName = v.className.split(/\s+/);
            for (var j = 0; j < splitClassName.length; j++) {
                var className = splitClassName[j];
                if (-1 === classes.indexOf(className)) {
                    classes.push(className);
                }
            }
        });
        if ('function' === typeof callback) {
            for (var i in classes) {
                callback(classes[i]);
            }
        }
        return classes;
    };
})(jQuery);
